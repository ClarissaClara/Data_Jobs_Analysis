/*
Question: What are the top skills based on salary for Data Analysts looking for remote opportunities?
- Calculate the average salary for each skill.
- Order the results by the average salary in descending order.
Purpose: Provide insights into the skills that are associated with higher salaries, 
helping job seekers understand which qualifications may lead to better compensation.
*/

SELECT
    skills,
    ROUND(AVG(salary_year_avg), 0) AS average_salary
FROM
    job_postings_fact jpf
INNER JOIN skills_job_dim sjd ON jpf.job_id = sjd.job_id
INNER JOIN skills_dim sd ON sjd.skill_id = sd.skill_id
WHERE
    job_title_short = 'Data Analyst' AND
    job_location = 'Anywhere' AND
    salary_year_avg IS NOT NULL
GROUP BY
    skills  
ORDER BY
    average_salary DESC
LIMIT 25;

